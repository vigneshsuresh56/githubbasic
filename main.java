class Main {
    public static void main(String[] args) {

        // Variables
        int a = 10;
        int b = 5;
        int sum;

        // Addition
        sum = a + b;
        System.out.println("Sum: " + sum);

        // Condition
        if (a > b) {
            System.out.println("A is greater than B");
        } else {
            System.out.println("B is greater");
        }

        // Loop
        for (int i = 1; i <= 5; i++) {
            System.out.println("Number: " + i);
        }
    }
}